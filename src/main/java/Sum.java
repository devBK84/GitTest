public class Sum {

    // public = für alle einsehbar
    // static = Methode einer Klase
    // (1.) int = Datentyp der zurückgeben wird
    // sum = Name der Methode
    // int a, int b) = Parameter der methode = die wir der Methode geht

    public static int sum (int a, int b) {
        return a + b;

    }

}
